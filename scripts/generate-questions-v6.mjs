// ---- 6. 展开 ----
const DIM_KEYS = ['sour','sweet','bitter','spicy','salty','rich','crunchy','tender'];
const SUF = ['a','b','c','d'];

function resolveOpt(o) {
  if (typeof o === 'string') {
    let v = dishMap[o];
    if (!v) v = citySign[o];
    if (!v) throw new Error('未知菜名/城市: ' + o);
    return { label: o, weights: { ...v } };
  }
  if (o && typeof o === 'object') {
    if ('neg' in o) {
      const v = { sour:0, sweet:0, bitter:0, spicy:0, salty:0, rich:0, crunchy:0, tender:0 };
      v[o.neg] = -50;
      return { label: o.l, weights: v };
    }
    if ('v' in o) return { label: o.l, weights: { ...o.v } };
  }
  throw new Error('无法解析选项: ' + JSON.stringify(o));
}

const questions = RAW.map(([id, type, stem, opts]) => ({
  id,
  stem,
  options: opts.map((o, i) => {
    const r = resolveOpt(o);
    return { id: `${id}-${SUF[i]}`, label: r.label, weights: r.weights };
  }),
}));

