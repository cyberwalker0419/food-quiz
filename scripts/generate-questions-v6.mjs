const probe = {}, neg = {}, counts = {};
DIM_KEYS.forEach(d => {
  probe[d] = questions.some(q => q.options.some(o => o.weights[d] >= 80));
  neg[d]   = questions.some(q => q.options.some(o => o.weights[d] <= -40));
  counts[d] = questions.filter(q => q.options.some(o => o.weights[d] !== 0)).length;
});
const cVals = Object.values(counts);

