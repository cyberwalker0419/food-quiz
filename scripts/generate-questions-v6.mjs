// ---- 3.5 底味均衡化：中国菜普遍因酱油/香料/发酵带微苦微酸底味，
//         但 vector 只记主味（多为 0）。补一个合理小非零值，使 8 维出场均衡（coverage 约束1）。
//         甜品补更小（焦糖/果酸微苦微酸），咸鲜辣菜补 6/5。小值不影响主味判断。----
function balance(v) {
  const isSweet = v.sweet >= 50 && v.salty < 35;
  const isSavory = v.salty >= 40 || v.spicy >= 30; // 咸辣菜才有酱油/香料苦底
  // bitter：只给咸辣菜补；清淡/甜品/主食/水果保持 0，作为剪枝后可选的中性题
  if (v.bitter === 0 && isSavory) v.bitter = 6;
  if (v.sour === 0)   v.sour   = isSweet ? 3 : 5;
  if (v.spicy === 0 && isSavory) v.spicy = 4; // 非甜品补姜/胡椒微辣底味
  return v;
}
Object.values(dishMap).forEach(balance);

