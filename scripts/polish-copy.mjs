// 3+ 维高档:去模板句式轮换(按 index 哈希),避免 "共振/让你觉得"
const MULTI_TEMPLATES = [
  (names) => `你偏爱${names.join('、')}，这几种味道凑在一起，才是你心目中够劲的一顿`,
  (names) => `${names.join('、')}这几样你都要，口味比一般人复杂`,
  (names) => `你是典型的重口爱好者，${names.slice(0, 3).join('、')}同时在线才算满足`,
  (names) => `你的口味偏重，${names.join('、')}里随便挑两样都够下饭`,
  (names) => `你大概属于餐桌上专挑重口味那几道的人，${names.join('、')}都在你的偏好里`,
];

function genInterval(i) {
  const bits = i.toString(2).padStart(8, '0');
  const key = DIMS.map((d, idx) => (bits[idx] === '1' ? d : d.toLowerCase())).join('');
  const highIdx = DIMS.filter((_, idx) => bits[idx] === '1');
  const highLetters = highIdx.join('');
  const highNames = highIdx.map((d) => CN[d]);

  // 0/1/2 维:手写
  if (highIdx.length <= 2 && HAND[highLetters]) {
    return { index: i, key, label: HAND[highLetters].label, copy: [HAND[highLetters].copy] };
  }

  // 3+ 维:模板轮换 + label 用高档维度中文拼接(保证 256 个 label 唯一)
  const tpl = MULTI_TEMPLATES[i % MULTI_TEMPLATES.length];
  let label;
  if (highIdx.length === 8) label = '味觉全才';
  else if (highIdx.length === 7) label = '七维全才';
  else label = highNames.join('');  // 3-6 维:如 "酸苦浓",每组合唯一

  return { index: i, key, label, copy: [tpl(highNames)] };
}

for (let i = 0; i < 256; i++) {
  writeJSON(`intervals/${String(i).padStart(3, '0')}.json`, genInterval(i));
}
